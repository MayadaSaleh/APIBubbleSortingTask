package pojo;

/**
 *
 * @author Mayada
 */
public class NumberPojo {

    private int id;
    private int number;

    public NumberPojo() {
    }

    public NumberPojo(int id, int number) {
        this.id = id;
        this.number = number;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

}
